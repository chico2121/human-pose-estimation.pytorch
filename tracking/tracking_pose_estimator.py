def pose_estimator(config, model, bbox_results, frame_num):

    # cudnn related setting
    cudnn.benchmark = config.CUDNN.BENCHMARK
    torch.backends.cudnn.deterministic = config.CUDNN.DETERMINISTIC
    torch.backends.cudnn.enabled = config.CUDNN.ENABLED

    gpus = [int(i) for i in config.GPUS.split(',')]

    # Data loading code
    normalize = transforms.Normalize(mean=[0.485, 0.456, 0.406],
                                     std=[0.229, 0.224, 0.225])

    image_path = os.path.join(config.TRACKING.DATASET, config.TRACKING.VIDEO_FILE_NAME)

    valid_dataset = VIDEODataset(
        config,
        config.TRACKING.ROOT,
        image_path,
        False,
        bbox_results,
        transforms.Compose([
            transforms.ToTensor(),
            normalize,
        ])
    )
    valid_loader = torch.utils.data.DataLoader(
        valid_dataset,
        batch_size=config.TEST.BATCH_SIZE*len(gpus),
        shuffle=False,
        num_workers=config.WORKERS,
        pin_memory=True
    )

    # evaluate on validation set
    return validate(config, valid_loader, valid_dataset, model, frame_num)

def validate(config, val_loader, val_dataset, model, frame_num):
    batch_time = AverageMeter()
    losses = AverageMeter()
    acc = AverageMeter()

    # switch to evaluate mode
    model.eval()

    num_samples = len(val_dataset)
    all_preds = np.zeros((num_samples, config.MODEL.NUM_JOINTS, 3), dtype=np.float32)
    all_boxes = np.zeros((num_samples, 6))
    image_path = []
    idx = 0
    with torch.no_grad():
        end = time.time()
        for i, (input, target, target_weight, meta) in enumerate(val_loader):
            # compute output
            output = model(input)
            if config.TEST.FLIP_TEST:
                # this part is ugly, because pytorch has not supported negative index
                # input_flipped = model(input[:, :, :, ::-1])
                input_flipped = np.flip(input.cpu().numpy(), 3).copy()
                input_flipped = torch.from_numpy(input_flipped).cuda()
                output_flipped = model(input_flipped)
                output_flipped = flip_back(output_flipped.cpu().numpy(),
                                           val_dataset.flip_pairs)
                output_flipped = torch.from_numpy(output_flipped.copy()).cuda()

                # feature is not aligned, shift flipped heatmap for higher accuracy
                if config.TEST.SHIFT_HEATMAP:
                    output_flipped[:, :, :, 1:] = \
                        output_flipped.clone()[:, :, :, 0:-1]
                    # output_flipped[:, :, :, 0] = 0

                output = (output + output_flipped) * 0.5

            num_images = input.size(0)

            # measure elapsed time
            batch_time.update(time.time() - end)
            end = time.time()

            _, avg_acc, cnt, pred = accuracy(output.cpu().numpy(), target.cpu().numpy())
            c = meta['center'].numpy()
            s = meta['scale'].numpy()
            score = meta['score'].numpy()

            preds, maxvals = get_final_preds(config, output.clone().cpu().numpy(), c, s)

            all_preds[idx:idx + num_images, :, 0:2] = preds[:, :, 0:2]
            all_preds[idx:idx + num_images, :, 2:3] = maxvals
            # double check this all_boxes parts
            all_boxes[idx:idx + num_images, 0:2] = c[:, 0:2]
            all_boxes[idx:idx + num_images, 2:4] = s[:, 0:2]
            all_boxes[idx:idx + num_images, 4] = np.prod(s*200, 1)
            all_boxes[idx:idx + num_images, 5] = score
            image_path.extend(meta['image'])

            idx += num_images

            queue_len_path = os.path.join(config.TRACKING.SAVE_IMAGE_PATH,"Qlen"+str(config.TRACKING.QUEUE_LEN))
            if not os.path.exists(queue_len_path):
                os.mkdir(queue_len_path)
            dir_name = os.path.join(queue_len_path, config.TRACKING.VIDEO_FILE_NAME)
            all_bb_images = os.path.join(dir_name, "all_bb")
            prefix = '{}{}_{}'.format(all_bb_images+'/', 'frame'+str(frame_num + 1), 'bb'+str(i))
            if not os.path.exists(dir_name):
                os.mkdir(dir_name)
            if not os.path.exists(all_bb_images):
                os.mkdir(all_bb_images)
            save_debug_images(config, input, meta, pred * 4, prefix)
        oks_nmsed_results = val_dataset.evaluate(all_preds, all_boxes, image_path)

    return oks_nmsed_results


class AverageMeter(object):
    """Computes and stores the average and current value"""
    def __init__(self):
        self.reset()

    def reset(self):
        self.val = 0
        self.avg = 0
        self.sum = 0
        self.count = 0

    def update(self, val, n=1):
        self.val = val
        self.sum += val * n
        self.count += n
        self.avg = self.sum / self.count if self.count != 0 else 0

